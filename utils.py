def compute_metrics(pred, wer_metric, cer_metric, tokenizer):
    pred_ids = pred.predictions
    label_ids = pred.label_ids

    # replace -100 with the pad_token_id
    label_ids[label_ids == -100] = tokenizer.pad_token_id

    # we do not want to group tokens when computing the metrics
    pred_str = tokenizer.batch_decode(pred_ids, skip_special_tokens=True)
    label_str = tokenizer.batch_decode(label_ids, skip_special_tokens=True)

    wer = 100 * wer_metric.compute(predictions=pred_str, references=label_str)
    cer = 100 * cer_metric.compute(predictions=pred_str, references=label_str)

    return {"wer": wer, "cer": cer}

def prepare_dataset(batch, tokenizer, feature_extractor):
    # load and resample audio data from 48 to 16kHz
    audio = batch["audio"]

    # compute log-Mel input features from input audio array 
    batch["input_features"] = feature_extractor(audio["array"], sampling_rate=audio["sampling_rate"]).input_features[0]

    # encode target text to label ids 
    batch["labels"] = tokenizer(batch["text"]).input_ids
    return batch

def prepare_dataset_aihub(batch, tokenizer, feature_extractor, sr=16000):
    
    # load and resample audio data from 48 to 16kHz
    audio, _ = librosa.load(batch['audio_path'], sr=sr)
    npy_path = batch['audio_path'].replace('wav', 'npy')
    input_features = feature_extractor(audio, sampling_rate=sr).input_features[0]
    np.save(npy_path, input_features)
    del input_features
    # compute log-Mel input features from input audio array 

    # encode target text to label ids 
    batch["labels"] = tokenizer(batch["text"]).input_ids
    batch["npy_path"] = npy_path
    gc.collect()
    return batch

def str2bool(v):
    if isinstance(v, bool):
        return v
    if v.lower() in ('yes', 'true', 't', 'y', '1'):
        return True
    elif v.lower() in ('no', 'false', 'f', 'n', '0'):
        return False
    else:
        raise argparse.ArgumentTypeError('Boolean value expected.')